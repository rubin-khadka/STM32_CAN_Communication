void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
  CAN_RxHeaderTypeDef RxHeader;
  uint8_t RxData[8];

  if(HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData) == HAL_OK)
  {
    // Check if it's our message (ID 0x200 with 2 data bytes)
    if(RxHeader.StdId == 0x200 && RxHeader.DLC == 2)
    {
      // Reconstruct 16-bit ADC value from 2 bytes
      uint16_t raw_value = (RxData[0] << 8) | RxData[1];

      // Scale 0-4095 (ADC) to 0-999 (PWM ARR)
      led_brightness = (raw_value * 999) / 4095;

      // Update PWM duty cycle
      __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, led_brightness);
    }
  }
}
